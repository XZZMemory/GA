def DrawCircle(radius, xShifting = 0, yShifting = 0, color='r'):
    '''
    画一个圆圈，radius半径 xShifting，yShifting圆心偏移量，color颜色
    不要问我为什么，就这么弄就能画出来
    '''
    print("开始画图")
    theta = np.linspace(0, 2*pi, 360)
    x = sin(theta)*radius+xShifting
    y = cos(theta)*radius + yShifting
    print("结束画图")
    plt.plot(x,y,color)
